function [xs, v1, v2] = va(x0, v10, v20, dt)
  xs(1) = x0(1) + dt*x0(3);
  xs(2) = x0(2) + dt*x0(4);
  xs(3) = x0(3);
  xs(4) = x0(4);
  v1(1) = v10(1) + dt*v10(3);
  v1(2) = v10(2) + dt*v10(4);
  v1(3) = v10(3);
  v1(4) = v10(4);
  v2(1) = v20(1) + dt*v20(3);
  v2(2) = v20(2) + dt*v20(4);
  v2(3) = v20(3);
  v2(4) = v20(4);
endfunction
